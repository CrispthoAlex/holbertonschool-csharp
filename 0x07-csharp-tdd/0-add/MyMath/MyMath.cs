using System;

namespace MyMath
{
    /// <summary>
    /// Operations class define an adding method
    /// </summary>
    public class Operations
    {
        /// <summary>
        /// Method to add the integers "a" and "b"
        /// <param name="a">integer</param>
        /// <param name="b">integer</param>
        /// <returns>Sum of a and b integers</returns>
        /// </summary>
        public static int Add(int a, int b)
        {
            return (a + b);
        }
    }
}
